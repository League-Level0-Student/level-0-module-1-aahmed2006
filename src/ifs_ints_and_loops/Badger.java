package ifs_ints_and_loops;

public class Badger {
	public static void main(String[] args) {
		String b = "Badger";
		String m = "Mushroom";
		String s = "Snake";
		for (int i = 0; i < 4; i++) {
			for (int a = 0; a < 10; a++) {
				System.out.println(b);
			}
			for (int a = 0; a < 2; a++) {
				System.out.println(m);
			}
		}
		for (int i = 0; i < 2; i++) {
			for (int a = 0; a < 10; a++) {
				System.out.println(b);
			}
			for (int a = 0; a < 5; a++) {
				System.out.println(s);
			}
		}
		for (int i = 0; i < 2; i++) {
			for (int a = 0; a < 10; a++) {
				System.out.println(b);
			}
			for (int a = 0; a < 2; a++) {
				System.out.println(m);
			}
		}
	}
}
